def greaterThan(x, y):
    if x > y :
        return True
    else:
        return False
print (greaterThan(3, 4))

def lessThan(x, y):
    if x < y :
        return True
    else:
        return False
print (lessThan(2, 3))

def equalTo(x, y):
    if x == y :
        return True
    else:
        return False
print (equalTo(3, 4))

def greaterOrEqual(x, y):
    if x >= y :
        return True
    else:
        return False
print (greaterOrEqual(3, 5))

def lessOrEqual(x, y):
    if x <= y :
        return True
    else:
        return False
print (lessOrEqual(5, 2))